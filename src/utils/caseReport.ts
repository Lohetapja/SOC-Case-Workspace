import type { SocCase } from '../types'
import {
  closureStatusLabels,
  confidenceLabels,
  entityTypeLabels,
  evidenceTypeLabels,
  findingCategoryLabels,
  findingStatusLabels,
  labDisclosureStateLabels,
  labWriteupStatusLabels,
  priorityLabels,
  questionStatusLabels,
  recommendationCategoryLabels,
  recommendationStatusLabels,
  severityLabels,
  sourceLabels,
  statusLabels,
  verdictLabels,
} from '../data/labels'
import { formatDateTime } from './format'
import { reviewCaseQuality } from './caseQuality'

/**
 * Turns a SocCase into a clean Markdown investigation report. Pure, read-only,
 * dependency-free, synthetic data only.
 */

type TitleMap = Map<string, string>

const isString = (value: string | undefined): value is string => Boolean(value)

function titlesFor(ids: string[] | undefined, map: TitleMap): string[] {
  return (ids ?? []).map((id) => map.get(id)).filter(isString)
}

export function buildCaseReport(socCase: SocCase): string {
  const evidenceTitle: TitleMap = new Map(socCase.evidence.map((item) => [item.id, item.title]))
  const timelineTitle: TitleMap = new Map(socCase.timeline.map((event) => [event.id, event.title]))
  const findingTitle: TitleMap = new Map(socCase.findings.map((finding) => [finding.id, finding.title]))

  const sections: string[] = [
    `# SOC Investigation Report: ${socCase.title}`,
    caseMetadata(socCase),
    `## Executive summary\n\n${socCase.summary || '_No executive summary has been recorded for this case._'}`,
    statusAndClassification(socCase),
    labTrainingContext(socCase),
    affectedEntities(socCase),
    evidenceSummary(socCase),
    timeline(socCase, evidenceTitle),
    decisionJournal(socCase),
    findings(socCase, evidenceTitle, timelineTitle),
    mitreMapping(socCase, findingTitle, evidenceTitle),
    recommendations(socCase),
    closureRationale(socCase),
    caseQualityLimitations(socCase),
    missingEvidence(socCase),
    '## Educational use and synthetic-data disclaimer\n\n_This report was generated by SOC Case Workspace, an educational portfolio tool. All data is synthetic — this is not a real security incident._',
  ]

  return `${sections.join('\n\n')}\n`
}

/** Safe download filename based on the case title and today's date. */
export function reportFilename(socCase: SocCase): string {
  const slug =
    socCase.title
      .toLowerCase()
      .replace(/[^a-z0-9]+/g, '-')
      .replace(/(^-|-$)/g, '')
      .slice(0, 60) || 'case'
  const date = new Date().toISOString().slice(0, 10)
  return `soc-report-${slug}-${date}.md`
}

function caseMetadata(socCase: SocCase): string {
  return [
    '## Case metadata',
    '',
    `- **Case ID:** ${socCase.id}`,
    `- **Severity:** ${severityLabels[socCase.severity]}`,
    `- **Status:** ${statusLabels[socCase.status]}`,
    `- **Source:** ${sourceLabels[socCase.source]}${socCase.sourceDetail ? ` (${socCase.sourceDetail})` : ''}`,
    `- **Owner:** ${socCase.owner}`,
    `- **Created:** ${formatDateTime(socCase.createdAt)}`,
    `- **Updated:** ${formatDateTime(socCase.updatedAt)}`,
  ].join('\n')
}

function statusAndClassification(socCase: SocCase): string {
  const closure = socCase.closure
  return [
    '## Assessment and disposition',
    '',
    `- **Workflow status:** ${statusLabels[socCase.status]}`,
    `- **Classification:** ${closure?.verdict ? verdictLabels[closure.verdict] : '_Not set_'}`,
    `- **Closure status:** ${closure?.closureStatus ? closureStatusLabels[closure.closureStatus] : '_Not set_'}`,
  ].join('\n')
}

function labTrainingContext(socCase: SocCase): string {
  const head = '## Lab / training context'
  const lab = socCase.lab
  if (!lab?.enabled) return `${head}\n\n_Not marked as a lab or training case._`
  const lines = [
    `- **Platform:** ${lab.platform || '_Not set_'}`,
    `- **Lab name:** ${lab.labName || '_Not set_'}`,
    `- **Writeup status:** ${labWriteupStatusLabels[lab.writeupStatus ?? 'not_started']}`,
    `- **Public writeup allowed:** ${labDisclosureStateLabels[lab.publicWriteupAllowed ?? 'unknown']}`,
    `- **Spoiler-sensitive:** ${labDisclosureStateLabels[lab.spoilerSensitive ?? 'unknown']}`,
  ]
  if (lab.scenarioSummary) lines.push(`- **Scenario summary:** ${lab.scenarioSummary}`)
  if (lab.toolsUsed) lines.push(`- **Tools used:** ${lab.toolsUsed}`)
  if (lab.learningNotes) lines.push(`- **Learning notes:** ${lab.learningNotes}`)
  return `${head}\n\n${lines.join('\n')}`
}

function affectedEntities(socCase: SocCase): string {
  const head = '## Affected entities'
  if (socCase.affectedEntities.length === 0) return `${head}\n\n_No affected entities have been recorded._`
  const rows = socCase.affectedEntities.map((entity) => {
    const context = [entity.role, entity.description].filter(Boolean).join(' — ')
    return `- **${entityTypeLabels[entity.type]}:** \`${entity.value}\`${context ? ` — ${context}` : ''}`
  })
  return `${head}\n\n${rows.join('\n')}`
}

function evidenceSummary(socCase: SocCase): string {
  const head = '## Evidence reviewed'
  if (socCase.evidence.length === 0) return `${head}\n\n_No evidence has been recorded._`
  const blocks = socCase.evidence.map((item) => {
    const meta = [
      evidenceTypeLabels[item.type],
      item.source,
      item.observedAt && formatDateTime(item.observedAt),
    ]
      .filter(Boolean)
      .join(' · ')
    const lines = [`- **${item.title}** (${meta})`, `  - ${item.detail}`]
    if (item.analystNote) lines.push(`  - _Note: ${item.analystNote}_`)
    return lines.join('\n')
  })
  return `${head}\n\n${blocks.join('\n')}`
}

function timeline(socCase: SocCase, evidenceTitle: TitleMap): string {
  const head = '## Timeline of activity'
  if (socCase.timeline.length === 0) return `${head}\n\n_No timeline events have been recorded._`
  const sorted = [...socCase.timeline].sort((a, b) => a.timestamp.localeCompare(b.timestamp))
  const rows = sorted.map((event) => {
    const evidence = titlesFor(event.relatedEvidenceIds, evidenceTitle)
    const suffix = evidence.length ? ` _(evidence: ${evidence.join(', ')})_` : ''
    return `- **${formatDateTime(event.timestamp)}** — **${event.title}**: ${event.description}${suffix}`
  })
  return `${head}\n\n${rows.join('\n')}`
}

function decisionJournal(socCase: SocCase): string {
  const head = '## Analyst reasoning and decision journal'
  if (socCase.analystQuestions.length === 0) return `${head}\n\n_No analyst questions or decisions have been recorded._`
  const blocks = socCase.analystQuestions.map((question) => {
    const lines = [`- **${question.question}** — _${questionStatusLabels[question.status]}_`]
    if (question.answer) lines.push(`  - Answer: ${question.answer}`)
    if (question.rationale) lines.push(`  - Rationale: ${question.rationale}`)
    return lines.join('\n')
  })
  return `${head}\n\n${blocks.join('\n')}`
}

function findings(socCase: SocCase, evidenceTitle: TitleMap, timelineTitle: TitleMap): string {
  const head = '## Findings'
  if (socCase.findings.length === 0) return `${head}\n\n_No analytical findings have been recorded._`
  const blocks = socCase.findings.map((finding) => {
    const tags = [
      finding.category && findingCategoryLabels[finding.category],
      finding.severity && `${severityLabels[finding.severity]} severity`,
      `${confidenceLabels[finding.confidence]} confidence`,
      finding.status && findingStatusLabels[finding.status],
    ]
      .filter(Boolean)
      .join(' · ')
    const lines = [`### ${finding.title}`, `_${tags}_`, '', finding.description]
    const evidence = titlesFor(finding.relatedEvidenceIds, evidenceTitle)
    if (evidence.length) lines.push('', `**Supporting evidence:** ${evidence.join(', ')}`)
    const events = titlesFor(finding.relatedTimelineEventIds, timelineTitle)
    if (events.length) lines.push(`**Related timeline:** ${events.join(', ')}`)
    return lines.join('\n')
  })
  return `${head}\n\n${blocks.join('\n\n')}`
}

function mitreMapping(socCase: SocCase, findingTitle: TitleMap, evidenceTitle: TitleMap): string {
  const head = '## MITRE ATT&CK assessment'
  if (socCase.mitreMappings.length === 0) return `${head}\n\n_No analyst-authored ATT&CK mappings have been recorded._`
  const blocks = socCase.mitreMappings.map((mapping) => {
    const meta = `${mapping.tactic ? `${mapping.tactic} · ` : ''}${confidenceLabels[mapping.confidence]} confidence`
    const lines = [`### ${mapping.techniqueId} — ${mapping.techniqueName}`, `_${meta}_`]
    if (mapping.rationale) lines.push('', `**Rationale:** ${mapping.rationale}`)
    const supportingFindings = titlesFor(mapping.relatedFindingIds, findingTitle)
    if (supportingFindings.length) lines.push(`**Supporting findings:** ${supportingFindings.join(', ')}`)
    const evidence = titlesFor(mapping.relatedEvidenceIds, evidenceTitle)
    if (evidence.length) lines.push(`**Supporting evidence:** ${evidence.join(', ')}`)
    return lines.join('\n')
  })
  return `${head}\n\n${blocks.join('\n\n')}`
}

function recommendations(socCase: SocCase): string {
  const head = '## Response recommendations and next actions'
  if (socCase.recommendations.length === 0) return `${head}\n\n_No response recommendations have been recorded._`
  const rows = socCase.recommendations.map((rec) => {
    const metadata = [
      rec.category && recommendationCategoryLabels[rec.category],
      `${priorityLabels[rec.priority]} priority`,
      rec.status && recommendationStatusLabels[rec.status],
    ].filter(Boolean).join(' · ')
    return `- **${rec.title}** (${metadata})${rec.description ? `: ${rec.description}` : ''}`
  })
  return `${head}\n\n${rows.join('\n')}`
}

function closureRationale(socCase: SocCase): string {
  const head = '## Closure assessment'
  const closure = socCase.closure
  if (!closure || (!closure.rationale && !closure.recommendedAction && !closure.impactSummary)) {
    return `${head}\n\n_No closure rationale, impact summary, or next action has been recorded._`
  }
  const lines: string[] = []
  if (closure.rationale) lines.push(`**Rationale:** ${closure.rationale}`)
  if (closure.recommendedAction) lines.push(`**Recommended action:** ${closure.recommendedAction}`)
  if (closure.impactSummary) lines.push(`**Impact:** ${closure.impactSummary}`)
  if (closure.closedAt) {
    lines.push(`**Closed:** ${formatDateTime(closure.closedAt)}${closure.closedBy ? ` by ${closure.closedBy}` : ''}`)
  }
  return `${head}\n\n${lines.join('\n\n')}`
}

function caseQualityLimitations(socCase: SocCase): string {
  const head = '## Case quality and review notes'
  const review = reviewCaseQuality(socCase)
  const lines = [
    `- **Completion:** ${review.completion.complete} / ${review.completion.total} checks complete (${review.completion.label})`,
    `- **Needs attention:** ${review.counts.warning}`,
    `- **Missing:** ${review.counts.missing}`,
  ]
  if (review.coachSuggestions.length > 0) {
    lines.push('', 'Reviewer notes:')
    lines.push(...review.coachSuggestions.map((suggestion) => `- ${suggestion}`))
  } else {
    lines.push('', '_No major quality gaps were flagged by the advisory review._')
  }
  return `${head}\n\n${lines.join('\n')}`
}

function missingEvidence(socCase: SocCase): string {
  const head = '## Investigation limitations and open questions'
  const open = socCase.analystQuestions
    .filter((question) => question.status === 'open')
    .map((question) => question.question)
  if (open.length === 0) {
    return `${head}\n\n_No open analyst questions are currently recorded. Validate any remaining evidence limitations before closure._`
  }
  return `${head}\n\nThe following analyst questions remain unresolved:\n\n${open.map((gap) => `- ${gap}`).join('\n')}`
}
